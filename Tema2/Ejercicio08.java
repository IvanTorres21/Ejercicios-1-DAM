/*
* Ejercicio 8, mostrar cinco chars que sean un string por pantalla
*/
public class Ejercicio08 {
  public static void main(String[] args) {
    char a = 'a';
    char r = 'r';
    char b = 'b';
    char o = 'o';
    char l = 'l';
    String arbol = "" + a + r + b + o + l;
    System.out.println("" + arbol);
  }
}
