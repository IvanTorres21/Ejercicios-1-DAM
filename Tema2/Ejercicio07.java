/*
* Ejercicio 7, mostrar varias Strings y Chars en la misma linea
*/
public class Ejercicio07 {
  public static void main(String[] args) {
    String fruta = "manzana";
    String sabor = "dulce";
    char a = 'a';
    char b = 'b';
    System.out.println("La " + fruta + " es " + sabor + "\n" + a + b);
  }
}
